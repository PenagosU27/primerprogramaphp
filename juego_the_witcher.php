<h1> The Witcher Game </h1>  

 <p> The Witcher (Wiedźmin en polaco) es una serie de videojuegos de RPG de acción y fantasía desarrollados por CD Projekt RED, basados en la saga de novelas de Geralt de Rivia, escritas por el autor polaco Andrzej Sapkowski. 
</p>  
<p> Basados en la saga de Geralt de Rivia, uno de los pocos brujos que quedan, asesinos de monstruos a sueldo con habilidades especiales. Los videojuegos cuentan las aventuras de Geralt a través de misiones que el jugador debe completar. 
</p> 
<p> The Witcher también tendrá una futura serie producida por Netflix que estrenará el 20 de diciembre de 2019.1​2​ Basada en las obras de la saga de Geralt de Rivia de Andrzej Sapkowski. La serie fue anunciada en 2017.
</p>  

